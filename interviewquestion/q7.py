# if we do any operation and we got answer as non zero number output will be "true" else "false"

a,b=12,5  #a=12 and b=5
if a+b:      #12+5 = 17( non zero >>> output will be true , so it will enter in to the "if")
    print('true')
else:
    print('false')



a,b=12,12  #a=12 and b=12
if a-b:      #12-12 = 0 (zero >>> output will be false , so it will enter in to the "else" )
    print('true')
else:
    print('false')