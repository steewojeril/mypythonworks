#relational operators
#used for comparison
#output of relational operators will be true or false

# >
# <
# <=
# >=
# ==
# !=

num1=int(input("enter num1"))  #10
num2=int(input("enter num2")) #20
print(num1>num2)  # output will be "false"

print(num1<num2)  # output will be "true"

print(num1<=num2)  # output will be "true"

print(num1>=num2)  # output will be "false"

print(num1==num2)  # output will be "false"

print(num1!=num2)  # output will be "true"



